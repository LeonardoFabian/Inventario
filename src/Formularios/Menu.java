    private void formKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_formKeyPressed
        // TODO add your handling code here:
        //System.out.println(evt.getKeyChar());
        System.out.println(evt.getKeyCode());
        if(evt.getKeyCode() == KeyEvent.VK_CONTROL){
            this.combinar[0] = KeyEvent.VK_CONTROL;
            
        }else if(evt.getKeyCode() == KeyEvent.VK_SHIFT){
            
        }else if(evt.getKeyCode() == KeyEvent.VK_U){
            this.combinar[1] = KeyEvent.VK_U;
            if((this.combinar[0] == 17) && (this.combinar[1] == 85)){
            this.usuario();
        }
        }else if(evt.getKeyCode() == KeyEvent.VK_C){
            this.combinar[1] = KeyEvent.VK_C;
            if((this.combinar[0] == KeyEvent.VK_CONTROL) && (this.combinar[1] == KeyEvent.VK_C)){
                this.cliente();
            }
        }else if(evt.getKeyCode() == KeyEvent.VK_P){
            this.combinar[1] = KeyEvent.VK_P;
            if((this.combinar[0] == KeyEvent.VK_CONTROL) && (this.combinar[1] == KeyEvent.VK_P)){
                this.Proveedor();
            }
        }else{
            System.out.println("4"+evt.getKeyCode());
            this.combinar[0] = 0;
            this.combinar[1] = 0;
        }
        /*
        if(evt.getKeyCode() == KeyEvent.VK_U){
            JOptionPane.showMessageDialog(null,""+evt.getKeyChar());
        }
        if(evt.getKeyCode() == KeyEvent.VK_CONTROL){
            JOptionPane.showMessageDialog(null,""+evt.getKeyChar());
        }
        if(evt.getKeyCode() == KeyEvent.VK_CONTROL && (evt.getKeyCode() == KeyEvent.VK_U)){
            JOptionPane.showMessageDialog(null,""+evt.getKeyChar());
        }
        */
        
    }//GEN-LAST:event_formKeyPressed

