    public void cliente(){
        if(this.administrador || this.cajero || this.supervisor){
            ClienteAdministrar cliente = new ClienteAdministrar(this.conector, this.usuarioID, this.usuarioNombre);
            cliente.setYo(cliente);
            cliente.setVisible(true);
        }
    }
    
    
    public void Proveedor(){
        if(this.administrador || this.supervisor ){
            ProveedorAdministrar proveedor = new ProveedorAdministrar(this.conector, this.usuarioID, this.usuarioNombre);
            proveedor.setYo(proveedor);
            proveedor.setVisible(true);
        }
    }
    
    
    public void Producto(){
        if(this.administrador || this.supervisor){
            ProductoAdministrar producto = new ProductoAdministrar(this.conector, this.usuarioID, this.usuarioNombre);
            producto.setVisible(true);
        }
    }
    
    
    public void ComprarProducto(){
        if(this.administrador || this.supervisor){
            CompraProductoAdministrar compra = new CompraProductoAdministrar(this.conector, this.usuarioID, this.usuarioNombre);
                compra.setYo(compra);
                compra.setVisible(true);            
        }
    }
    
    
    public void usuario(){
        if(this.administrador || this.cajero || this.supervisor){
            UsuariosAdministrar usuario = new UsuariosAdministrar(this.conector,this.usuarioID,this.usuarioNombre);
            usuario.setVisible(true);
        }
    }
    
    public void acercade(){
        if(this.administrador || this.cajero || this.supervisor){
            AcercaDe acercaDe = new AcercaDe();
            acercaDe.setVisible(true);
        }
    }
    
