    public void validarTipoUsuario(){
        administrador = false;
        supervisor = false;
        cajero = false;
        switch(this.usuarioTipo){
            case "administrador":
                this.administrador = true;
                break;
            case "supervisor":
                this.supervisor = true;
                break;
            case "cajero":
                this.cajero = true;
                break;
        }
    }
    
